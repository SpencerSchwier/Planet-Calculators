package com.techelevator.ssg.model.calculators;

public class Transportation {
	
	private String name;
	private int speed;
	
	public Transportation(String name, int speed) {
		this.name = name;
		this.speed = speed;
	}
	
	public String getName() {
		return name;
	}

	public int getSpeed() {
		return speed;
	}
	

}
